    <script>
      $(document).ready(function () {
        // Auto-fill form when modal opens from any source
        $("#appointment").on("show.bs.modal", function (e) {
          const trigger = e.relatedTarget;
          const pricingPlan = $(trigger).attr("data-pricing-plan");
          const serviceType = $(trigger).attr("data-service-type");
          const projectTitle = $(trigger).attr("data-project-title");
          const isContactBtn =
            $(trigger).hasClass("contact-btn") ||
            $(trigger).hasClass("contact-us-btn");

          // Map pricing plans to form values
          const pricingPlanMap = {
            "basic-ecommerce": "basic-ecommerce",
            "inventory-module": "inventory-module",
            "accounts-module": "accounts-module",
            "crm-module": "crm-module",
            "mlm-module": "mlm-module",
            "custom-development": "custom-development",
          };

          // Handle pricing plan from Get Started buttons
          if (pricingPlan) {
            $("#quoteBudget").val(pricingPlanMap[pricingPlan] || "");
            const pricingPlanNames = {
              "basic-ecommerce": "Basic E-commerce - 30K BDT",
              "inventory-module": "Inventory Module - 40K BDT",
              "accounts-module": "Accounts Module - 50K BDT",
              "crm-module": "CRM Module - 60K BDT",
              "mlm-module": "MLM Module - 70K BDT",
              "custom-development": "Custom Software Development - Negotiable",
            };
            const description = `Pricing Plan: ${pricingPlanNames[pricingPlan]}\n\nYour domain name: [Please enter your domain]\n\nOther Information:\n- Business type: [Please specify]\n- Current challenges: [Please describe]\n- Timeline: [Please mention]\n\nPlease provide additional details about your requirements.`;
            $("#quoteMessage").val(description);
          } else if (isContactBtn || serviceType || projectTitle) {
            // Handle other contact button types
            if (serviceType) {
              $("#quoteService").val(serviceType);
            } else {
              $("#quoteService").val("custom-software");
            }

            // Auto-fill project description
            let description = "";
            if (projectTitle) {
              description = `I want to discuss custom software development and software optimization for ${projectTitle}. This project needs professional development services.`;
            } else {
              description =
                "I want to discuss custom software development and software optimization for our project.";
            }
            $("#quoteMessage").val(description);
          }
        });

        // Form validation and submission
        $("#quoteForm").on("submit", function (e) {
          e.preventDefault();

          // Check form validity
          if (!this.checkValidity()) {
            e.stopPropagation();
            $(this).addClass("was-validated");
            return false;
          }

          // Collect form data and map to API field names
          const formData = {
            full_name: $("#quoteName").val(),
            email: $("#quoteEmail").val(),
            phone: $("#quotePhone").val(),
            company_name: $("#quoteCompany").val(),
            service: $("#quoteService").val(),
            pricing_plan: $("#quoteBudget").val(),
            description: $("#quoteMessage").val(),
          };

          // Disable submit button to prevent double submission
          const $submitButton = $(this).find('button[type="submit"]');
          const originalButtonText = $submitButton.html();
          $submitButton
            .prop("disabled", true)
            .html(
              '<span class="spinner-border spinner-border-sm me-2" role="status" aria-hidden="true"></span>Submitting...',
            );

          // Submit via API
          $.ajax({
            url: '{{ url("/api/v1/ecommerce-orders/store") }}',
            type: "POST",
            contentType: "application/json",
            data: JSON.stringify(formData),
            headers: {
              "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content"),
              Accept: "application/json",
            },
            success: function (response) {
              // Show success message
              alert(
                "Thank you! Your quote request has been submitted successfully. We will contact you within 24 hours.",
              );

              // Reset form
              $("#quoteForm")[0].reset();
              $("#quoteForm").removeClass("was-validated");

              // Close modal
              $("#appointment").modal("hide");

              // Re-enable button
              $submitButton.prop("disabled", false).html(originalButtonText);
            },
            error: function (xhr) {
              // Re-enable button
              $submitButton.prop("disabled", false).html(originalButtonText);

              // Handle validation errors
              if (
                xhr.status === 422 &&
                xhr.responseJSON &&
                xhr.responseJSON.errors
              ) {
                let errorMessage = "Please correct the following errors:\n";
                $.each(xhr.responseJSON.errors, function (field, messages) {
                  errorMessage += "\n- " + messages.join(", ");
                });
                alert(errorMessage);
              } else {
                // Show generic error message
                alert(
                  "Sorry, there was an error submitting your request. Please try again or contact us directly.",
                );
              }
            },
          });
        });
      });
    </script>

